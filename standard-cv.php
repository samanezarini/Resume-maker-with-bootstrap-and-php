                            <p style="text-align: justify;">رزومه کاری باید رنگ و لعاب خوبی داشته باشد تا در بین رزومه های دیگر بدرخشد. اما زیبایی محتوا هم خیلی مهم است و فرم و محتوای رزومه باید هر دو زیبا باشند.&nbsp;<br>خطای دوم رزومه نویسی، رعایت نکردن خوانایی رزومه است. فونت غیررسمی و ناخوانا، یعنی پایان کار شما برای استخدام در آن شرکت! برای اینکه این خطا را مرتکب نشوید، حتماً رزومه را به اطرافیان نشان دهید و از ایشان نظرخواهی کنید.</p>
