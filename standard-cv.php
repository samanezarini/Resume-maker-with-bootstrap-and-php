                            <p style="text-align: justify;">خیر؛&nbsp;قالب های رزومه رایگان زیادی در اینترنت وجود دارد. به راحتی با گوگل کردن فایل رزومه می توانید به منابع زیادی از سایت های رزومه ساز برسید. ما نیز برای راحتی کارجویان، تعدادی فایل رزومه فارسی و انگلیسی را در قالب های متنوع Word وPDF آماده کرده ایم. از طریق لینک زیر خیلی راحت به <a href="../../DownloadResume">مرکز دانلود رزومه</a> سایت ما دست خواهید یافت.</p>
