                            <p style="text-align: justify;">هرگز این خطای بزرگ را در رزومه نویسی مرتکب نشوید. هر رزومه برای یک فرصت شغلی است.&nbsp;<br>البته منظور این نیست که هر بار یک رزومه جدید بنویسید. گاهی با بالا و پایین کردن اطلاعات موضوع حل می شود و گاهی هم باید&nbsp;برخی محتویات را حذف و محتویات جدیدی را در رزومه درج کنید. ساخت رزومه کاری باید کاملاً با توجه به ملاحظات درج شده در هر آگهی استخدام باشد. پس ببینید برای هر شرکت که آگهی جذب نیروی کار داده، چه چیزهایی در درجه بالاتری از اهمیت قرار دارد.<br>مثال: یک کارجو می خواهد برای شغل برنامه نویس رزومه بنویسد و سابقه کار&nbsp;در زمینه های زیر را دارد:<br>تدریس زبان – بازاریاب - پشتیبان نرم افزار - مونتاژ دستگاه CNC - طراحی سایت<br>موارد 1، 2 و 3 هیچ ارتباطی به کار جدید او ندارند، پس این فرد در رزومه کاری اش تنها باید موارد 3 و 5 را وارد نماید.</p>
                            <br />
