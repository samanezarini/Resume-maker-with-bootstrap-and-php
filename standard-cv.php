                                <li style="text-align: justify;">در شرایط برابر بین اضلاع رزومه کاری و زمانی که دست شما برای هر بخش رزومه پر است، باید ببنید که شرکت ها و سازمان ها چه رزومه ای می خواهند و برایشان چه گزینه هایی&nbsp;مهم تر است.</li>
                            </ol>
                            <br />
                            <h5 class="text-info">پرسش 12:&nbsp;در قسمت مشخصات فردی رزومه چه اطلاعاتی باید نوشت؟</h5>
                            <p style="text-align: justify;">مشخصات شخصی به معنی اسم کامل، عکس رسمی، وضعیت خدمت سربازی، وضعیت تاهل، تاریخ تولد، محل اقامت، شماره تلفن ثابت، شماره موبایل و پست الکترونیکی است.</p>
                            <br />
                            <h5 class="text-info">پرسش 13:&nbsp;در قسمت درباره ی من چه حرف هایی را بنویسم؟</h5>
                            <p style="text-align: justify;">این قسمت&nbsp;در واقع گُلِ رزومه است. کارسازترین و قیمتی ترین کلمات رزومه خود را در دو سه جمله تجمیع کنید و به عنوان درباره ی من، در رزومه یادداشت کنید. این بخش اصلاً ربطی به بیوگرافی های شبکه اجتماعی ندارد و اینجا باید طور دیگری فکر کنید و خودتان را به شیوه دیگری معرفی کنید.</p>
                            <br />
                            <h5 class="text-info">پرسش 14:&nbsp;آیا درج عکس پرسنلی در بالای رزومه الزامی است؟</h5>
                            <p style="text-align: justify;">در ایران هیچ الزامی برای گذاشتن یا نگذاشتن عکس در رزومه وجود ندارد و کاملاً به نظر شخصی خودتان بستگی دارد. ولی در کل کارفرمای ایرانی رزومه با تصویر شخصی را بیشتر ترجیح می دهد.&nbsp;</p>
                            <br />
                            <h5 class="text-info">پرسش 15:&nbsp;آیا همه سوابق تحصیلی باید در رزومه آورده شود؟</h5>
                            <p style="text-align: justify;">برای رزومه کاری همه سوابق دانشگاهی لازم نیست. اگر سطح تحصیلات شما در حد دکتری است فقط دکتری و فوق لیسانس را بنویسید.&nbsp;<br>در ضمن اگر دانشگاه رفته اید و حداقل مدرک فوق دیپلم دارید، سوابق درسی قبل از آن را به طور کامل فراموش کنید.</p>
                            <br />
                            <h5 class="text-info">پرسش 16:&nbsp;سوابق کاری به چه ترتیبی باید در رزومه نوشته شود؟</h5>
                            <p style="text-align: justify;">اول از همه باید چند تا سابقه شغلی خیلی نزدیک به فرصت کار جدید را جدا کنید. نهایتاً سه چهار مورد کافی است. بعد آن شغل هایی که تاریخ نزدیک تری دارند را بنویسند و پس از آن شغل های قدیمی تر را در رزومه درج کنید.</p>
                            <br />
                            <h5 class="text-info">پرسش 17:&nbsp;چه مهارت هایی باید در رزومه گنجانده شود؟</h5>
                            <p style="text-align: justify;">برخی&nbsp;مهارت ها فقط مخصوص کار است که حتماً آن ها را در کار خود می شناسید. اگر هم نمی شناسید، کافی است یک دور آگهی های استخدام را مرور کنید تا ببینید شرکت ها چه تخصص هایی را می خواهند.<br>دسته&nbsp;دیگر&nbsp;مهارت ها هم مانند توانایی های شخصی و اجتماعی جزو مهارت های زندگی یا مهارت های نرم است. مثلاً خوب صحبت کردن یا همان فن بیان، توانایی برقراری ارتباط و تعامل در گروه های کاری و تعهد کاری مثال هایی از این مهارت های نرم هستند.&nbsp;</p>
                            <br />
                            <h5 class="text-info">پرسش 18:&nbsp;گواهینامه هایی که دارم را در کجای رزومه بنویسم؟</h5>
                            <p style="text-align: justify;">گواهینامه ها را نزدیک به قسمت مهارت ها و توانمندی ها بنویسید. در واقع گواهینامه ها هم مهارت حساب می شوند. در ضمن اگر گواهینامه یک مهارت را دارید، تکرار آن هیچ ایرادی ندارد و حتماً آن در هر دو بخش رزومه کاری تان بنویسید.</p>
                            <br />
                            <h5 class="text-info">پرسش 19:&nbsp;آیا باید مدارکم را به رزومه ضمیمه کنم؟</h5>
                            <p style="text-align: justify;">خیر! زمان ارایه مدارک و ضمایم رزومه کاری، روز حضورتان در شرکت است. پس هیچ فایلی را به ضمیمه رزومه نفرستید و تنها همان رزومه کاری را ارسال کنید.</p>
                            <br />
                            <h5 class="text-info">پرسش 20:&nbsp;آیا در رزومه کاری، بخش سرگرمی ها و علاقه مندی های شخصی داریم؟</h5>
                            <p style="text-align: justify;">اصلاً برای دیگران، آن هم کسی که می خواهد به شما دستمزد دهد، مهم نیست که زندگی شخصی شما چگونه است. یک شرکت یا سازمان می خواهد ببیند شما ارزش استخدام شدن را دارید یا نه؟ پس طوری در رزومه حرف بزنید که مدیر شرکت قانع شود شما نیروی کار به درد بخوری هستید و می توانید باری از کارهای شرکت را به دوش بکشید.</p>
                            <br />
                            <h5 class="text-info">پرسش 21:&nbsp;رزومه تحصیلی یا سی وی (CV) چه شباهت ها و چه تفاوت هایی با رزومه کاری (Resume) دارد؟</h5>
