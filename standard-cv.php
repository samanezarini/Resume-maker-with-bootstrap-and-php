                            <p style="text-align: justify;">ما برای تصمیم گیری در هر زمینه نیاز به جمع آوری اطلاعات داریم. خب حالا چطور یک سازمان یا شرکت راجع به انتخاب گزینه های استخدام تصمیم می گیرد؟ از اطلاعاتی که رزومه کاری تان در اختیار آن ها قرار می دهد. پس برای استخدام، داشتن یک رزومه اجباری است.</p>
